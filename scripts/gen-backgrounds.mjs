// Scans public/images/backgrounds/ and writes the ordered list of background
// images to src/lib/backgrounds.generated.ts so the app can rotate them every
// two weeks. Runs as part of the build, so dropping images in the folder +
// redeploying is all that's needed — no manual code edits.
import { readdirSync, writeFileSync, existsSync, mkdirSync } from "node:fs";
import { join } from "node:path";

const dir = join(process.cwd(), "public", "images", "backgrounds");
const out = join(process.cwd(), "src", "lib", "backgrounds.generated.ts");
const exts = new Set([".jpg", ".jpeg", ".png", ".webp", ".svg"]);

let files = [];
if (existsSync(dir)) {
  files = readdirSync(dir)
    .filter((f) => exts.has(f.slice(f.lastIndexOf(".")).toLowerCase()))
    .sort((a, b) => a.localeCompare(b))
    .map((f) => `/images/backgrounds/${f}`);
}

if (!existsSync(join(process.cwd(), "src", "lib"))) mkdirSync(join(process.cwd(), "src", "lib"), { recursive: true });

const body =
  "// AUTO-GENERATED by scripts/gen-backgrounds.mjs — do not edit by hand.\n" +
  `export const BACKGROUND_IMAGES: string[] = ${JSON.stringify(files, null, 2)};\n`;
writeFileSync(out, body);
console.log(`gen-backgrounds: ${files.length} image(s) → src/lib/backgrounds.generated.ts`);
