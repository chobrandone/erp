// Pulls fresh maritime / logistics background image links from Unsplash and
// writes them to src/lib/backgrounds.online.generated.ts. These are merged with
// the bundled images and rotated every two weeks by src/lib/backgrounds.ts.
//
// Why Unsplash: it's the best free source for high-resolution, hotlink-friendly
// photos on maritime / port / logistics themes — exactly the look this ERP wants
// — with a simple JSON API and no per-image licensing hassle.
//
// Usage (needs a free Unsplash "Access Key" from https://unsplash.com/developers):
//   UNSPLASH_ACCESS_KEY=xxxxx node scripts/fetch-online-backgrounds.mjs
//
// Run it on a schedule (e.g. a GitHub Action every 2 weeks) so it commits the
// regenerated file and triggers a redeploy — that's the "auto-refresh after a
// few weeks" part. Without the key it is a no-op and the file is left as-is.
import { writeFileSync } from "node:fs";
import { join } from "node:path";

const OUT = join(process.cwd(), "src", "lib", "backgrounds.online.generated.ts");
const KEY = process.env.UNSPLASH_ACCESS_KEY;

// Curated search terms that match the ERP's container-depot / shipping theme.
const QUERIES = ["container ship port", "cargo terminal", "shipping logistics", "container yard crane"];
const PER_QUERY = 3;
const TARGET = 12;
const SIZE = "&w=1920&q=80&auto=format&fit=crop";

function banner(msg) {
  console.log(`fetch-online-backgrounds: ${msg}`);
}

async function main() {
  if (!KEY) {
    banner("UNSPLASH_ACCESS_KEY not set — skipping (bundled images will be used).");
    return;
  }

  const urls = new Set();
  for (const q of QUERIES) {
    try {
      const res = await fetch(
        `https://api.unsplash.com/search/photos?query=${encodeURIComponent(q)}&orientation=landscape&content_filter=high&per_page=${PER_QUERY}`,
        { headers: { Authorization: `Client-ID ${KEY}`, "Accept-Version": "v1" } },
      );
      if (!res.ok) {
        banner(`query "${q}" failed: HTTP ${res.status}`);
        continue;
      }
      const data = await res.json();
      for (const photo of data.results ?? []) {
        if (photo?.urls?.raw) urls.add(`${photo.urls.raw}${SIZE}`);
        // Per Unsplash API guidelines, trigger the download endpoint (fire-and-forget).
        if (photo?.links?.download_location) {
          fetch(photo.links.download_location, { headers: { Authorization: `Client-ID ${KEY}` } }).catch(() => {});
        }
      }
    } catch (err) {
      banner(`query "${q}" error: ${err.message}`);
    }
  }

  const list = [...urls].slice(0, TARGET);
  if (list.length === 0) {
    banner("no images returned — leaving the existing file unchanged.");
    return;
  }

  const body =
    "// AUTO-GENERATED by scripts/fetch-online-backgrounds.mjs — do not edit by hand.\n" +
    "// Maritime / logistics photos pulled from Unsplash, hotlinked and refreshed\n" +
    "// on a schedule. Attribution: photos courtesy of Unsplash (https://unsplash.com).\n" +
    `export const ONLINE_BACKGROUND_IMAGES: string[] = ${JSON.stringify(list, null, 2)};\n`;
  writeFileSync(OUT, body);
  banner(`wrote ${list.length} online image link(s) → src/lib/backgrounds.online.generated.ts`);
}

main();
